    private void tampil_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Kode Siswa");
        model.addColumn("Nama");
        model.addColumn("Tanggal Lahir");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Alamat");
        model.addColumn("Nomor Telepon");
        model.addColumn("Email");
        
        try{
            int no=1;
            String sql = "SELECT * FROM siswa";
            java.sql.Connection conn = (Connection)koneksi.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{
                    no++,
                    res.getString("kode_siswa"),
                    res.getString("nama"),
                    res.getString("tanggal_lahir"),
                    res.getString("jenis_kelamin"),
                    res.getString("alamat"),
                    res.getString("nomor_telepon"),
                    res.getString("email"),
                });
            }
            tabelsiswa.setModel(model);
        }catch (SQLException e){
            System.out.println("ERROR : " + e.getMessage());
        }
    }
    
    public static Date getTanggalLahir(JTable table, int kolom){
        JTable tabel = table;
        String str_tgl = String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), kolom));
        Date tgl = null;
        try{
            tgl = new SimpleDateFormat("yyy-MM-dd").parse(str_tgl);
        }catch(ParseException ex){
            Logger.getLogger(DataSiswa.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        return tgl;
    }
