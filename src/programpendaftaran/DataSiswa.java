            String sql = "INSERT into siswa VALUES ('"+kodeTF.getText()+"','"+namaTF.getText()+"','"+tgl+"','"+jeniskelamin+"','"+alamatTF.getText()+"','"+teleponTF.getText()+"','"+emailTF.getText()+"')";
