    private void tampil_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Kode Pendaftaran");
        model.addColumn("Kode Siswa");
        model.addColumn("Kode Kelas");
        model.addColumn("Tanggal Pendaftaran");
        model.addColumn("Biaya Pendaftaran");
        model.addColumn("Jurusan");
        model.addColumn("Status");
        
        
        try{
            int no =1;
            String sql = "SELECT * FROM pendaftaran";
            java.sql.Connection conn =(Connection)koneksi.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{
                    no++,
                    res.getString("kode_pendaftaran"),
                    res.getString("kode_siswa"),
                    res.getString("kode_kelas"),
                    res.getString("tanggal_pendaftaran"),
                    res.getString("biaya_pendaftaran"),
                    res.getString("jurusan"),
                    res.getString("status"),
                });
            }
            tabelpendaftaran.setModel(model);
        }catch (SQLException e){
            System.out.println("ERROR : " + e.getMessage());
        }
    }

    public static Date getTanggalPendaftaran(JTable table, int kolom){
        JTable tabel = table;
        String str_tgl = String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), kolom));
        Date tanggal = null;
        try{
            tanggal = new SimpleDateFormat("yyyy-MM-dd").parse(str_tgl);
            
        }catch(ParseException ex){
        Logger.getLogger(DataPendaftaran.class.getName()).log(Level.SEVERE, null,ex);
            
        }
        return tanggal;
    }
