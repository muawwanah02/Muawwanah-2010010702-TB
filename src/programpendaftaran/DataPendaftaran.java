    private void simpanBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBTActionPerformed
        // TODO add your handling code here:
        String tampilan = "yyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggal = String.valueOf(fm.format(tglTF.getDate()));
        try{
            String sql = "INSERT INTO pendaftaran VALUES ('"+kodePTF.getText()+"','"+kodeSTF.getText()+"','"+kodeKTF.getText()+"','"+tanggal+"','"+biayaTF.getText()+"','"+jurusanCB.getSelectedItem()+"','"+statusCB.getSelectedItem()+"')";
            java.sql.Connection conn =(Connection)koneksi.koneksiDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            tampil_data();
            bersih_data();
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_simpanBTActionPerformed

    private void tabelpendaftaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpendaftaranMouseClicked
        // TODO add your handling code here:
        int baris = tabelpendaftaran.rowAtPoint(evt.getPoint());
        String kodeP=tabelpendaftaran.getValueAt(baris, 1).toString();
        kodePTF.setText(kodeP);
        String kodeS=tabelpendaftaran.getValueAt(baris, 2).toString();
        kodeSTF.setText(kodeS);
        String kodeK=tabelpendaftaran.getValueAt(baris, 3).toString();
        kodeKTF.setText(kodeK);
        tglTF.setDate(getTanggalPendaftaran(tabelpendaftaran, 4));
        String biaya=tabelpendaftaran.getValueAt(baris, 5).toString();
        biayaTF.setText(biaya);
        String jurusan=tabelpendaftaran.getValueAt(baris, 6).toString();
        jurusanCB.setSelectedItem(jurusan);
        String status=tabelpendaftaran.getValueAt(baris, 7).toString();
        statusCB.setSelectedItem(status);
        
        
    }//GEN-LAST:event_tabelpendaftaranMouseClicked

