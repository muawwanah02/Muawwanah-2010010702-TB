    private void tabelkelasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelkelasMouseClicked
        // TODO add your handling code here:
        int baris = tabelkelas.rowAtPoint(evt.getPoint());
        String kodekelas = tabelkelas.getValueAt(baris, 1).toString();
        kodeTF.setText(kodekelas);
        String namakelas = tabelkelas.getValueAt(baris, 2).toString();
        namaTF.setText(namakelas);
        String tahunajaran = tabelkelas.getValueAt(baris, 3).toString();
        tahunTF.setText(tahunajaran);
        String kapasitas = tabelkelas.getValueAt(baris, 4).toString();
        kapasitasTF.setText(kapasitas);
    }//GEN-LAST:event_tabelkelasMouseClicked

