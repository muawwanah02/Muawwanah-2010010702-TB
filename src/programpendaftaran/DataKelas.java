    private void tampil_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Kode Kelas");
        model.addColumn("Nama Kelas");
        model.addColumn("Tahun Ajaran");
        model.addColumn("Kapasitas");
        
        try{
            int no =1;
            String sql = "SELECT * FROM kelas";
            java.sql.Connection conn = (Connection)koneksi.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{
                    no++,
                    res.getString("kode_kelas"),
                    res.getString("nama_kelas"),
                    res.getString("tahun_ajaran"),
                    res.getString("kapasitas"),
            });
            }
            tabelkelas.setModel(model);
        }catch (SQLException e){
            System.out.println("ERROR : " + e.getMessage());
        }
    }
